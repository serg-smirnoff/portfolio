<h1>Дополнительные конструкции</h1>

<div id="text">

<p>

<img align="right" width="350" src="img/c01.jpg" height="443" /><font size="2">Часто архитектура и дизайн дома или коттеджа требуют нестандартного вида дверного блока.</font>

</p>

<p>

<font size="2"><b>Боковины и фрамуги.</b><br />

Распространённые конструкции, устанавливаются в проходах к лифтам, в тамбурах, квартирах и т.д., если размеры проёма больше стандартных.</font>

</p>

<p>

<font size="2">Боковины (цельная с рамой конструкция) и фрамуги (отдельная конструкция) выполнены из формованного профиля, имеют внутри рёбра жёсткости и лист металла (t = 2 мм) с двух сторон, окрашиваются только порошковой краской, заполняются минеральной ватой. На боковинах могут быть установлены вызывные панели аудио-видеодомофонов.</font>

</p>

<p>

<font size="2"><b>Двустворчатая дверь. </b><br />

Красивое и удобное решение проблемы с нестандартным проёмом. Конструкция подвижной створки, в отличие от боковин и фрамуг, позволяет крепить любую отделку - от шпона до массива дуба, проносить крупногабаритные предметы.</font>

</p>

<p>

<font size="2"><b>Запирающий механизм "Удав". </b><br />

Система "безлюфтовой фиксации" на основе дифференциального метода - принципиально новое устройство для особо надёжного, взломозащитного запирания подвижной створки двустворчатой двери. Оно позволяет несколькими оборотами простой отвертки минимальными усилиями с помощью клиновидных ригелей прижать створку к уплотнителям с силой в несколько сот килограмм. При этом обеспечивается исключительная плотность прилегания (отсутствует дребезг и вибрация) и гарантируется надёжная звуко-термоизоляция по периметру створк.</font>

</p>

<p>

<b style="clear: right; display: block"><font size="1">Запирающий механизм "Удав" <br />

(вид сверху)</font></b>

</p>

