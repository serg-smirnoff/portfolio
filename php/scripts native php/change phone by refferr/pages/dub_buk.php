<h1>Коллекции отделок</h1>

<div id="text">

<p>

<b><font size="2">Массив дуба, бука.</font></b>

</p>

<p>

<font size="2"><img align="right" width="403" src="img/j1.jpg" height="509" />Никакая имитация не заменит атмосферы комфорта, создаваемой натуральным деревом. Специальная технология изготовления панелей позволяет древесине "дышать". Высокая степень защищённости к воздействию атмосферных осадков, устойчивость к перепаду температур позволяет панелям сохранять цвет и целостность даже при эксплуатации в постоянном контакте с внешней средой.</font>

</p>

<p>

<font size="2">При изготовлении панели используется специальная технология сборки и нанесения лакокрасочных материалов, которая позволяет древесине сохранять свой первозданный вид. Сборка производится с компенсационными зазорами в филёнках, препятствующими растрескиванию древесины при впитывании влаги. Нанесение защитного лака предохраняет панель от воздействия ультрафиолетовых лучей и, следовательно, от изменения цвета панелк на солнце.</font>

</p>

<p>

<font size="2">Гармоничный ансамбль панели и портала из ценных пород дерева не только украсит любое помещение но и будет служить Вам долгие годы.</font>

</p>

<p>

<font size="2"><b>Порталы.*</b>Монтируются после установки дверного блока и выведения черновых откосов, а изготавливаются так же как и панели, из массива ценных пород дерева.</font>

