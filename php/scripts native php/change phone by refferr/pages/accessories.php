<h1>Фурнитура</h1>

<div id="text">

<p align="justify">

<img align="right" width="347" src="img/g1.jpg" height="440" /><font size="2">С помощью дополнительных опций любой замок может стать более защищенным и красивым.</font>

</p>

<p align="justify">

<font size="2"><b>Защита.</b> Броненакладки (далее "БН") предназначены для защиты цилиндрового механизма от механических видов воздействия. Мы рекомендуем установку врезных "БН". Такой тип обеспечивает лучшую защиту цилиндра.<br />

Помимо защиты от взлома, "БН" может защитить цилиндр и от засорения замочной скважины. Эта возможность появилась благодаря тому, что колпак "БН" стал выполнять не только декоративную, но и защитную функцию и открывается магнитным ключом.</font>

</p>

<p>

<font size="2">В нашем ассортименте есть ещё и кодовые накладки, защищающие сувальдную часть замка от вскрытия отмычками.</font>

</p>

<p>

<font size="2"><b>Красота.</b> Декоративные накладки на замок, глазки, шторки для глазков и ручки производят разные специализированные заводы. Все эти поставщики имеют свой ассортимент продукции, свои методы нанесения покрытий и, в связи с этим, долгое время подобрать для одной двери фурнитуру одного цвета (кроме латуни и хрома) не представлялось возможным. Теперь эта проблема решена. Покрытия на все виды фурнитуры наносятся на фабрике "PASINI" (Италия), благодаря чему обеспечивается единство цветов и высокое качество. Посещая многие европейские выставки, ведя изыскательскую работу и покупая новое оборудование, мы продолжаем работать над тем, чтобы воплотить в жизнь всё, что придумают наши клиенты и их дизайнеры.</font>

</p>

