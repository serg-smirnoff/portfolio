<h1 align="justify"><font size="3">Цилиндры</font></h1>

<p align="left">

<font size="2"><img align="right" width="381" src="img/f1.jpg" height="482" style="width: 381px; height: 554px" />В 1847 году Лайнусом Йейллом был изобретён цилиндровый механизм секретности. Новый механизм обладал рядом преимуществ, главными из которых были большее, по сравнению с сувальдным замком, количество комбинаций, при одновременном уменьшении размеров ключа. Принцип, изобретенный Лайнусом Йейллом, продолжает развиваться конструкторами компании CISA. Вековой опыт компании в производстве замков, исследования в области безопасности и современные технологии позволили создать один из самых совершенных цилиндровых механизмов секретности. Этот цилиндр называется CISA RS-3. Он практически не поддаётся вскрытию отмычками, благодаря почти бесконечному количеству комбинаций ключа и исключает снятие слепка, благодаря наличию на ключе плавающего элемента.</font>

</p>

<p align="left">

<font size="2">Не смотря на то, что корпус цилиндра оснащён защитой от высверливания, выбивания и слома, стоит заметить, что с основным недостатком всех цилиндровых механизмов - слабой защищённостью от механического воздействия - окончательно справиться не удалось. Поэтому, если Вы выбрали цилиндровый механизм секретности для свой двери, обязательно обеспечьте ему дополнительную защиту с помощью броненак-ладки.</font>

</p>

<p align="left">

<font size="2">Особое внимание хотелось бы обратить на новинки, <strong>цилиндры CISA RS3 и ASTRAL в исполнении три в одном.</strong> Если раньше по окончании ремонта или при потере ключей необходимо было менять весь механизм секретности, то теперь появились цилиндры, которые можно просто перекодировать. Операция перекодировки очень проста и мастера вызывать не обязательно.</font>

</p>

